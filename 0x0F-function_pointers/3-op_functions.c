/**
 * op_add - adds two int
 * @a: int 1
 * @b: int 2
 * Return: sum of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts two int
 * @a: int 1
 * @b: int 2
 * Return: subtration of a and b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two int
 * @a: int 1
 * @b: int 2
 * Return: multiplication of a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two numbers
 * @a: the first number
 * @b: the second number
 * Return: division of a and b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - calculates remainder of two int
 * @a: the first number
 * @b: the second number
 * Return: the remainder of a and b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
